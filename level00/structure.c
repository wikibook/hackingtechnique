#include <stdio.h>

/* function( ) 함수 정의 */
void function(int a, int b, int c) { // int 타입의 a, b, c 인자를 받음
	char buffer1[5]; 			// char 타입의 buffer 배열을 지역변수로 선언
	char buffer2[10]; 			// char 타입의 buffer2 배열을 지역변수로 선언
}

int main( ) {
	function(1,2,3); 			// function( ) 함수에 인자로 1, 2, 3를 전달하면서 호출
	return 0;
}